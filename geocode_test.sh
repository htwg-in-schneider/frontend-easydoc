# URL encode function for German addresses
urlencode() {
  local input="$1"
  # Replace spaces with +
  input="${input// /+}"
  # Replace German umlauts
  input="${input//ä/%C3%A4}"
  input="${input//ö/%C3%B6}"
  input="${input//ü/%C3%BC}"
  input="${input//Ä/%C3%84}"
  input="${input//Ö/%C3%96}"
  input="${input//Ü/%C3%9C}"
  input="${input//ß/%C3%9F}"
  # Replace commas with +
  input="${input//,/+}"
  echo "$input"
}

